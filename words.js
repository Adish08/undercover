let playedIndices = new Set();

export function getRandomWordPair() {
  if (playedIndices.size >= WORD_DATABASE.length) {
    playedIndices.clear();
  }

  let randomIndex;
  let attempts = 0;
  do {
    randomIndex = Math.floor(Math.random() * WORD_DATABASE.length);
    attempts++;
  } while (playedIndices.has(randomIndex) && attempts < 100);

  playedIndices.add(randomIndex);
  const pair = WORD_DATABASE[randomIndex];
  
  if (Math.random() > 0.5) {
    return { genre: pair.genre, wordA: pair.wordB, wordB: pair.wordA };
  }
  return pair;
}
